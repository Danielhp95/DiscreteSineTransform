void createSN(double **sn, double N) {
  int i, k;
  for (i = 1; i < N; i++) {
    for (k = 1; k < N; k++) {
      sn[i][k] = sin( (((double)i*k)*PI) /N );
    }
  }
}

void createTN(double **tn, double N) {
  int i, k;
  for (i = 1; i <= N; i++) {
    for (k = 1; k <= N; k++) {
      tn[i][k] = sin( ((double)(2*i-1)*k)*PI /(2*N) );
    }
  }
}

void createUN(double **un, double N) {
  int i, k;
  for (i = 1; i <= N; i++) {
    for (k = 1; k <= N; k++) {
      un[i][k] = sin( ((double)(2*i-1)*(2*k-1)*PI) /(4*N) );
    }
  }
}

